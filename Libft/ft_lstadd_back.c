#include "libft.h"

void	ft_lstadd_back(t_list **lst, t_list *new)
{
	t_list	*aux;

	aux = *lst;
	while (aux->next)
	{
		aux = aux->next;
	}
	aux->next = new;
	new->next = NULL;
}
