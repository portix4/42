#include "libft.h"
#include <stddef.h>
#include <stdio.h>

static char	*ft_substr(char const *s, unsigned int start, size_t len);

static int	ft_count_words(char const *s, char c)
{
	int	i;
	int	count;

	i = 0;
	count = 0;
	while (s[i])
	{
		while (s[i] == c)
			i++;
		if (s[i])
			count++;
		while (s[i] && s[i] != c)
			i++;
	}
	return (count);
}

char	**ft_split(char const *s, char c)
{
	char	**tab;
	int	i;
	int	j;
	int	k;

	i = 0;
	j = 0;
	if (!s)
		return (NULL);
	tab = (char **)malloc(sizeof(char *) * (ft_count_words(s, c) + 1));
	if (!tab)
		return (NULL);
	while (s[i])
	{
		while (s[i] == c)
			i++;
		k = i;
		while (s[i] && s[i] != c)
			i++;
		if (i > k)
		{
			tab[j] = ft_substr(s, k, i - k);
			if (!tab[j])
				return (NULL);
			j++;
		}
	}
	tab[j] = '\0';
	return (tab);
}
